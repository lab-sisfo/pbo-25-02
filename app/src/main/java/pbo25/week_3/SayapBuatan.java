package FlyingCow;

public class SayapBuatan {
    private String bahan;
    private int lebar;

    public SayapBuatan(String bahan, int lebar) {
        this.bahan = bahan;
        this.lebar = lebar;
    }

    public String getBahan() { return bahan; }
    public int getLebar() { return lebar; }
}